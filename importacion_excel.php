        protected function AddInsertColumns(Grid $grid)
        {
            //
            // Edit column for url_archivo field
            //
            $editor = new ImageUploader('url_archivo_edit');
            $editor->SetShowImage(false);
            $editor->setAcceptableFileTypes('.xlsx');
            $editColumn = new UploadFileToFolderColumn('Documento Excel', 'url_archivo', $editor, $this->dataset, false, false, 'data_extermal/importacion_excel/', '%random%.%original_file_extension%', $this->OnGetCustomUploadFilename, false);
            $editColumn->SetReplaceUploadedFileIfExist(true);
            $validator = new RequiredValidator(StringUtils::Format($this->GetLocalizerCaptions()->GetMessageString('RequiredValidationMessage'), $this->RenderText($editColumn->GetCaption())));
            $editor->GetValidatorCollection()->AddValidator($validator);
            $this->ApplyCommonColumnEditProperties($editColumn);
            $grid->AddInsertColumn($editColumn);
            $grid->SetShowAddButton(true && $this->GetSecurityInfo()->HasAddGrant());
        }
    
